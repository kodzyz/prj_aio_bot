    # –∏–∑ —Å–ª–æ–≤–∞—Ä—è —Å –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã–º–∏ –¥–∞–Ω–Ω—ã–º–∏ –∏–∑–≤–ª–µ–∫–∞–µ–º id –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    # —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –≤—Å–µ —Ç–æ–≤–∞—Ä—ã –∫–∞—Ç–µ–≥–æ—Ä–∏–∏

    products = db.fetchall('''SELECT * FROM products product
     WHERE product.tag = (SELECT title FROM categories WHERE idx=?) ''', (category_idx,))
    # –∑–∞–ø—Ä–æ—Å –Ω–∞ –ø–æ–ª—É—á–µ–Ω–∏–µ –≤—Å–µ—Ö —Ç–æ–≤–∞—Ä–æ–≤ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –ø–æ –µ–µ id

    await query.message.delete()
    await query.answer('–í—Å–µ –¥–æ–±–∞–≤–ª–µ–Ω–Ω—ã–µ —Ç–æ–≤–∞—Ä—ã –≤ —ç—Ç—É –∫–∞—Ç–µ–≥–æ—Ä–∏—é.')
    # –£–¥–∞–ª—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ –∏ –≤—ã–≤–æ–¥–∏–º –Ω–æ–≤–æ–µ

    await state.update_data(category_index=category_idx)
    # –¥–æ–ø–æ–ª–Ω–µ–Ω–∏—è –∫–æ–Ω—Ç–µ—Å—Ç–∞ –Ω–æ–≤—ã–º –∞—Ä–≥—É–º–µ–Ω—Ç–æ–º id –∫–∞—Ç–µ–≥–æ—Ä–∏–∏

