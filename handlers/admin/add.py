async def set_category_title_handler(message: Message, state: FSMContext):
    category = message.text
    # –ø–æ–ª—É—á–∞–µ–º –≤–≤–µ–¥–µ–Ω–Ω–æ–µ –∞–¥–º–∏–Ω–æ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –Ω–æ–≤–æ–π –∫–∞—Ç–µ–≥–æ—Ä–∏–∏

