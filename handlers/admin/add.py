    # –ò–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–º –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –±—É–¥–µ—Ç –µ–µ –∑–∞—Ö–µ—à–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ

    db.query('INSERT INTO categories VALUES (?, ?)', (idx, category))
    # –∑–∞–ø–∏—Å—å idx, title –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö

    await state.finish()
    # –≤—ã–∫–ª —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ (–ø—Ä–æ—Ü–µ—Å—Å —Å–æ–∑–¥–∞–Ω–∏—è –∫–∞—Ç–µ–≥–æ—Ä–∏–∏)

